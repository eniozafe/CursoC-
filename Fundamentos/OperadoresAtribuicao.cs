using System;
using System.Collections.Generic;
using System.Text;

namespace CursoCSharp.Fundamentos
{
    class OperadoresAtribuicao
    {
        public static void Executar()
        {
            var num1 = 3;
            num1 += 10; // num1 = num1 + 10
            num1 -= 10; // num1 = num1 - 10
            num1 *= 10; // num1 = num1 * 10
            num1 /= 10; // num1 = num1 / 10

        }
    }
}
